package com.kzw.vo;

import java.util.List;

import com.kzw.pojo.TbStrategy;
import com.kzw.pojo.TbTimes;

public class StrategyVo {

	private TbStrategy strategy;
	private List<TbTimes> timesList;
	public TbStrategy getStrategy() {
		return strategy;
	}
	public void setStrategy(TbStrategy strategy) {
		this.strategy = strategy;
	}
	public List<TbTimes> getTimesList() {
		return timesList;
	}
	public void setTimesList(List<TbTimes> timesList) {
		this.timesList = timesList;
	}
	public StrategyVo() {
		super();
	}
	public StrategyVo(TbStrategy strategy, List<TbTimes> timesList) {
		super();
		this.strategy = strategy;
		this.timesList = timesList;
	}
	
}
