package com.kzw.vo;

import java.util.List;

import com.kzw.pojo.TbStore;
import com.kzw.pojo.TbUser;

/**
 * 显示店铺信息和合伙人信息
 * @author 子煜
 *
 */
public class CustomShowStoreInf {

	/**
	 * 用于判断权限 -> 是否是店主
	 */
	private String limits;
	/**
	 * 店主的信息
	 */
	private TbUser user;
	/**
	 * 店铺的信息
	 */
	private TbStore storeInf;
	/**
	 * 店铺合伙人的信息
	 */
	private List<PartnerPo> partnerLists;
	public String getLimits() {
		return limits;
	}
	public void setLimits(String limits) {
		this.limits = limits;
	}
	public TbUser getUser() {
		return user;
	}
	public void setUser(TbUser user) {
		this.user = user;
	}
	public TbStore getStoreInf() {
		return storeInf;
	}
	public void setStoreInf(TbStore storeInf) {
		this.storeInf = storeInf;
	}
	public List<PartnerPo> getPartnerLists() {
		return partnerLists;
	}
	public void setPartnerLists(List<PartnerPo> partnerLists) {
		this.partnerLists = partnerLists;
	}
	public CustomShowStoreInf() {
		super();
	}
	public CustomShowStoreInf(String limits, TbUser user, TbStore storeInf,
			List<PartnerPo> partnerLists) {
		super();
		this.limits = limits;
		this.user = user;
		this.storeInf = storeInf;
		this.partnerLists = partnerLists;
	}
	
}
